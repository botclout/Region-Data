package RegionView;

public enum Collision {
    OPEN(0),
    WALL_NORTHWEST                  (0b0000000000000000000000000000001),
    WALL_NORTH                      (0b0000000000000000000000000000010),
    WALL_NORTHEAST                  (0b0000000000000000000000000000100),
    WALL_EAST                       (0b0000000000000000000000000001000),
    WALL_SOUTHEAST                  (0b0000000000000000000000000010000),
    WALL_SOUTH                      (0b0000000000000000000000000100000),
    WALL_SOUTHWEST                  (0b0000000000000000000000001000000),
    WALL_WEST                       (0b0000000000000000000000010000000),
    OBJECT_TILE                     (0b0000000000000000000000100000000),

    WALL_BLOCK_NORTHWEST            (0b0000000000000000000001000000000),
    WALL_BLOCK_NORTH                (0b0000000000000000000010000000000),
    WALL_BLOCK_NORTHEAST            (0b0000000000000000000100000000000),
    WALL_BLOCK_EAST                 (0b0000000000000000001000000000000),
    WALL_BLOCK_SOUTHEAST            (0b0000000000000000010000000000000),
    WALL_BLOCK_SOUTH                (0b0000000000000000100000000000000),
    WALL_BLOCK_SOUTHWEST            (0b0000000000000001000000000000000),
    WALL_BLOCK_WEST                 (0b0000000000000010000000000000000),

    OBJECT_BLOCK                    (0b0000000000000100000000000000000),
    DECORATION_BLOCK                (0b0000000000001000000000000000000),
    MAP_BLOCK                       (0b0000000001000000000000000000000),

    WALL_ALLOW_PROJECTILE_NORTHWEST (0b0000000010000000000000000000000),
    WALL_ALLOW_PROJECTILE_NORTH     (0b0000000100000000000000000000000),
    WALL_ALLOW_PROJECTILE_NORTHEAST (0b0000001000000000000000000000000),
    WALL_ALLOW_PROJECTILE_EAST      (0b0000010000000000000000000000000),
    WALL_ALLOW_PROJECTILE_SOUTHEAST (0b0000100000000000000000000000000),
    WALL_ALLOW_PROJECTILE_SOUTH     (0b0001000000000000000000000000000),
    WALL_ALLOW_PROJECTILE_SOUTHWEST (0b0010000000000000000000000000000),
    WALL_ALLOW_PROJECTILE_WEST      (0b0100000000000000000000000000000),
    OBJECT_ALLOW_PROJECTILE         (0b1000000000000000000000000000000);

    public final int flag;

    Collision(int flag) {
        this.flag = flag;
    }

    public boolean test(int clip) {
        return (flag & clip) == flag;
    }

}
